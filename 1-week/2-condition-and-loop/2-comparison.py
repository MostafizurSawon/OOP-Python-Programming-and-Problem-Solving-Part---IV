# 6 types of comparison

# < checks whether smaller || > checks whether bigger

# <= checks whether equal or less || >= checks whether equal or greater

# == checks whether both the values are equal || != checks whether both the values are not equal

print(12 < 20)

print(10 > 10)

print('a' < 'b')

print(10 >= 10)

my_money = 20
your_money = 30
print(my_money == your_money)
print(my_money != your_money)
